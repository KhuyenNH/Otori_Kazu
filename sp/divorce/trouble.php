                                <p class="answerText">結婚する前から持っていた財産は、結婚期間に共同で築いた財産にあたりませんので、基本的には財産分与の対象外となります。従って、結婚前から所有する不動産は、離婚の財産分与の対象にはならず、所有権も変わりません。<br>しかし、結婚後に住宅ローンを組んで自宅を購入して、住宅ローンが残っている場合は支払いが滞ると銀行が抵当権を行使して、自宅が競売にかけられる可能性があります。<br>①　評価額よりも住宅ローン残額が多い場合<br>離婚時の評価額が4,000万円、ローン残額が5,000万円の場合、売却しても1,000万円の住宅ローンが残るため、資産価値はないとされ、財産分与の対象とはなりません。所有者が返済を継続していくことになります。<br>②　評価額よりもローン残額が少ない場合<br>離婚時の評価額が4,000万円、ローン残額が3,000万円の場合、売却すれば1,000万円の売却代金が残るため、資産価値は1,000万円と考えられ、1,000万円が財産分与の対象となります。一般的な分与の方法としては、自宅を売却して得た、1,000万円を2分の1（500万円）ずつ分けることが多いですが、夫婦のどちらかが自宅を持ち続ける代わりに、相手方に500万円を支払うこともあります。</p>
