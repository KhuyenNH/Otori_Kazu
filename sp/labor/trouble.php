                                <p class="answerText"> 労働基準法には、管理職という地位の規定はなく、「管理監督者」が管理職に相当すると考えられています。労働基準法では、一般労働者に対しては、時間外労働や休日出勤の割増賃金について規定されていますが、管理監督者には適用されません。<br>一方で、実質的な業務内容が管理監督者にあたらないのに、役職を理由に残業代を支払わなかった企業に対し、東京地裁がこれを支払うよう判決を下したいわゆる「名ばかり管理職」については、記憶に新しいところです。なお、管理監督者であっても、深夜残業の割増賃金や年次有給休暇の規定は適用が除外されません。<br>以下は、管理監督者に該当するためのメルクマールです。判断基準は厳格で、一般企業の役員相当が該当すると思われます。</p>
